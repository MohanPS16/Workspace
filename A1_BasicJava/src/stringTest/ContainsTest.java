package stringTest;

public class ContainsTest {
	public static void main(String[] args) {
	String a= "Indian Railway";
	System.out.println(a.contains("Railway"));
	
	String b= "Mohan Parasuraman Shanthi";
	System.out.println(b.contains("Shanthi"));
	System.out.println(b.contains("Mohan"));
	
	String n= "My Sisters Nisha & Rohini";
	System.out.println(n.contains("Nisha"));
	System.out.println(n.contains("Rohini"));
	System.out.println(n.contains("my"));
	
}
}
