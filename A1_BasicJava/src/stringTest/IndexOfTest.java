package stringTest;

public class IndexOfTest {
	public static void main(String[] args) {
	
		
		
		String a= "Rattan";
		String b= "Rattan India Power";
		String c= "Revolt Motors";
		
		System.out.println(a.indexOf("Ra"));
		System.out.println(b.indexOf("In"));
		System.out.println(c.indexOf("vo"));
		
		System.out.println(a.lastIndexOf("an"));
		System.out.println(b.lastIndexOf("wer"));
		System.out.println(c.lastIndexOf("to"));
		
	}

}
